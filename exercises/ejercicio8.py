# hagamos un programa que pida al usuario dos numeros

# pidamos el primer numero (string)
n1 = input("cual es el primer numero")

# convertamos el string recibido a un dato numerico
n1 = int(n1)

# pidamos el segundo número 
n2 = input("cual es el segundo numero")

# convertimos el segundo string recibido a un dato númerico
n2 = int(n2)

# esos dos numeros hay que sumarlos
suma = n1 + n2

# mostrar suma
print("la suma es", suma)